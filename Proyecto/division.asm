ORG 100H
JMP INICIO 

NO1 DB 8D ;DIVIDENDO 
NO2 DB 3D ;DIVISOR
RES DB 0 ;RESIDUO
COS DB 0 

INICIO: 
MOV AX,0 
MOV CX,1 
INT 10H ;INICIALIZA PANTALLA 

MOV BL,NO2
MOV AL,NO1
DIV BL 
MOV RES,AH
MOV BL,0 
 
D:  
CMP AL,0AH
JL SIGUE
INC BL
SUB AL,0AH
JMP D   

SIGUE:
CMP BL,0
JZ U  
MOV COS,AL
MOV AL,BL
MOV AH,0AH
ADD AL,30H
INT 10H 
MOV AL,COS
INC DL

U: 
MOV AH,02H
INT 10H      
ADD AL,30H
MOV AH,0AH
INT 10H 

PUNTO:
MOV AH,02H
INC DL
INT 10H  
MOV AH,0AH 
MOV AL,2EH 
INT 10H 

MOV AL,0AH
MUL RES
MOV BL,NO2
DIV BL 
MOV RES,AH
INC DL
MOV AH,02H
INT 10H      
ADD AL,30H
MOV AH,0AH
INT 10H   
MOV AL,0AH
MUL RES
MOV BL,NO2
DIV BL 
MOV RES,AH
INC DL
MOV AH,02H
INT 10H      
ADD AL,30H
MOV AH,0AH
INT 10H 



